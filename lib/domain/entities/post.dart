class Post {
  final String uuid;
  final String id;
  final String username;
  final String title;
  final String content;
  final String time;
  final String createdAt;
  final String updatedAt;

  Post({
    required this.uuid,
    required this.id,
    required this.username,
    required this.title,
    required this.content,
    required this.time,
    required this.createdAt,
    required this.updatedAt,
  });
}


