class TotalTransactions {
  final String id;
  final String userId;
  final int year;
  final int month;
  final double totalIncome;
  final double totalExpenses;
  final double balance;

  TotalTransactions({
    required this.id,
    required this.userId,
    required this.year,
    required this.month,
    required this.totalIncome,
    required this.totalExpenses,
    required this.balance,
  });
}
